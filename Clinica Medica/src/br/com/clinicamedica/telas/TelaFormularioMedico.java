            jTextFieldNome.setText("");
            jTextFieldEmail.setText("");

            jFormattedTextFielCpf.setText("");
            jFormattedTextFieldCelular.setText("");
            jFormattedTextFieldCrm.setText("");
            jFormattedTextFieldRg.setText("");
            jFormattedTextFieldTelefone.setText("");

            jComboBoxEspecialidade.setSelectedIndex(-1);
            jTextFieldNome.setEnabled(false);
            jTextFieldEmail.setEnabled(false);

            jFormattedTextFielCpf.setEnabled(false);
            jFormattedTextFieldCelular.setEnabled(false);
            jFormattedTextFieldCrm.setEnabled(false);
            jFormattedTextFieldRg.setEnabled(false);
            jFormattedTextFieldTelefone.setEnabled(false);

            jComboBoxEspecialidade.setEnabled(false);
            jButtonCancelar.setEnabled(true);
            jButtonSalvar.setEnabled(false);
            TelaFormularioMedico tela = new TelaFormularioMedico();
            tela.setVisible(true);
