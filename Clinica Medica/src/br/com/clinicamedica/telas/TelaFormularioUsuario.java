    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed

        String message = "Voce deseja realmente  excluir?";
        int reply = JOptionPane.showConfirmDialog(null, message, "", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION) {
            usuario.setIdUsuario(Integer.parseInt(jTextFieldIdUsuario.getText()));
            usuarioDAO.excluir(usuario);
            jTextFieldIdUsuario.setText("");
            jTextFieldNome.setText("");
            jTextFieldUsuario.setText("");
            jPasswordFieldSenha2.setText("");
            jComboBoxUsuario.setSelectedIndex(-1);
            jPasswordFieldSenha.setText("");
            jTextFieldNome.setEnabled(false);
            jTextFieldUsuario.setEnabled(false);
            jPasswordFieldSenha.setEnabled(false);
            jComboBoxUsuario.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            jButtonEditar.setEnabled(false);
            jButtonExcluir.setEnabled(false);
            TelaFormularioUsuario tela = new TelaFormularioUsuario();
            tela.setVisible(true);
            dispose();
        }

    }//GEN-LAST:event_jButtonExcluirActionPerformed

