    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // TODO add your handling code here:
        jTextFieldPesquisa.requestFocus();
        if (jTextFieldPesquisa.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Vazio");
            jTableListaPaciente.setToolTipText(" ");
            jTextFieldNomePaciente.setText("");

        } else {

            Vector<Paciente> listaNomes = pacienteDAO.consultar(jTextFieldPesquisa.getText());
            Vector conjuntoLinhas = new Vector();

            for (Paciente pacien : listaNomes) {
                Vector linha = new Vector();

                linha.add(pacien.getNome());
                linha.add(pacien.getRg());
                linha.add(pacien.getCpf());
                linha.add(pacien.getCelular());
                linha.add(pacien.getTelefone());
                linha.add(pacien.getNasc());
                conjuntoLinhas.add(linha);

            }
            Vector conjuntoColunas = new Vector();
            conjuntoColunas.add("NOME");
            conjuntoColunas.add("RG");
            conjuntoColunas.add("CPF");
            conjuntoColunas.add("CELULAR");
            conjuntoColunas.add("TELEFONE");
            conjuntoColunas.add("NASCIMENTO");

            DefaultTableModel tabela = new DefaultTableModel(conjuntoLinhas, conjuntoColunas);

            jTableListaPaciente.setModel(tabela);

            jTableListaPaciente.getColumnModel().getColumn(0).setResizable(false);
            jTableListaPaciente.getColumnModel().getColumn(1).setResizable(false);
            jTableListaPaciente.getColumnModel().getColumn(2).setResizable(false);
            jTableListaPaciente.getColumnModel().getColumn(3).setResizable(false);
            jTableListaPaciente.getColumnModel().getColumn(4).setResizable(false);
            jTableListaPaciente.getColumnModel().getColumn(5).setResizable(false);
            jTableListaPaciente.getTableHeader().setReorderingAllowed(false);
            jTableListaPaciente.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

