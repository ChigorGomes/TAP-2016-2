    private void jButtonPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisaActionPerformed
        usuario.setBusca(jTextFieldPesquisa.getText());

        if (jTextFieldPesquisa.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Vazio!");
            listaNome();
            jTextFieldNome.setText("");
            jTextFieldUsuario.setText("");
            jPasswordFieldSenha.setText("");
            jTextFieldIdUsuario.setText("");
            jComboBoxUsuario.setSelectedIndex(-1);
            jTextFieldNome.setEnabled(false);
            jTextFieldUsuario.setEnabled(false);
            jPasswordFieldSenha.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jButtonExcluir.setEnabled(false);
            jButtonEditar.setEnabled(false);
        } else {
            Vector<Usuario> listaNomes = usuarioDAO.consultarPaciente(jTextFieldPesquisa.getText());
            Vector conjuntoLinhas = new Vector();
            for (Usuario us : listaNomes) {
                Vector linha = new Vector();
                linha.add(us.getNome());
                linha.add(us.getTipoUsuario());
                conjuntoLinhas.add(linha);

            }
            Vector conjuntoColunas = new Vector();
            conjuntoColunas.add("USUARIO");
            conjuntoColunas.add("TIPO USUARIO");
            DefaultTableModel tabela = new DefaultTableModel(conjuntoLinhas, conjuntoColunas);
            jTableListaUsuario.setModel(tabela);
            jTableListaUsuario.getColumnModel().getColumn(0).setResizable(false);
            jTableListaUsuario.getColumnModel().getColumn(1).setResizable(false);
            jTableListaUsuario.getTableHeader().setReorderingAllowed(false);
            jTableListaUsuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        }
    }//GEN-LAST:event_jButtonPesquisaActionPerformed

    private void jTableListaUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableListaUsuarioMouseClicked
        if (jTableListaUsuario.getSelectedRow() >= 0) {
            String nome = "" + jTableListaUsuario.getModel().getValueAt(jTableListaUsuario.getSelectedRow(), 0);
            usuarioDAO.acessaTabela(usuario, nome);
            jTextFieldIdUsuario.setText(String.valueOf(usuario.getIdUsuario()));
            jComboBoxUsuario.setSelectedItem(usuario.getTipoUsuario());
            jTextFieldNome.setText(usuario.getNome());
            jTextFieldUsuario.setText(usuario.getUsuario());
            jPasswordFieldSenha.setText((usuario.getSenha()));
            jPasswordFieldSenha2.setText(usuario.getSenha());
            jButtonEditar.setEnabled(true);
            jButtonCancelar.setEnabled(true);
            jButtonExcluir.setEnabled(true);
            jButtonSalvar.setEnabled(false);
        }
    }//GEN-LAST:event_jTableListaUsuarioMouseClicked

