    private void jButtonCadastraConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastraConsultaActionPerformed
        // TODO add your handling code here:

        if (jComboBoxMedico.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "Selecione o médico");
        } else if (jTextFieldNomePaciente.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Informe o usuario");
        } else if (jComboBoxHorario.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "informe o horário!");

        } else if (jDateChooserData.getDate() == null) {

            JOptionPane.showMessageDialog(null, "informe a data!");
        } else if (jTextAreaObs.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "informe a observacao!");

        } else if (jComboBoxMedico.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Informe o médico");
        } else {
            consulta.setHorario((String) jComboBoxHorario.getSelectedItem());
            consulta.setNomePac(jTextFieldNomePaciente.getText());
            consulta.setNomeMed(jTextField1.getText());
            consulta.setObservacoes(jTextAreaObs.getText());
            consulta.setDataConsulta(jDateChooserData.getDate());
            consulta.setStatus("Aberto");
            consultaDAO.controleAgendamento(consulta, medico.getIdMedico());

            dispose();

        }


    }//GEN-LAST:event_jButtonCadastraConsultaActionPerformed

