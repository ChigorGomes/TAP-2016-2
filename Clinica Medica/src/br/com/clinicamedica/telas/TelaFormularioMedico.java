    private void jButtonPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisaActionPerformed
        medico.setBusca(jTextFieldPesquisa.getText());

        if (jTextFieldPesquisa.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Vazio!");
            listaNome();
            jTextFieldNome.setText("");
            jTextFieldEmail.setText("");

            jFormattedTextFielCpf.setText("");
            jFormattedTextFieldCelular.setText("");
            jFormattedTextFieldCrm.setText("");
            jFormattedTextFieldRg.setText("");
            jFormattedTextFieldTelefone.setText("");

            jTextFieldidMedico.setText("");
            jComboBoxEspecialidade.setSelectedIndex(-1);
            jTextFieldNome.setEnabled(false);
            jTextFieldEmail.setEnabled(false);

            jFormattedTextFielCpf.setEnabled(false);
            jFormattedTextFieldCelular.setEnabled(false);
            jFormattedTextFieldCrm.setEnabled(false);
            jFormattedTextFieldRg.setEnabled(false);
            jFormattedTextFieldTelefone.setEnabled(false);

            jComboBoxEspecialidade.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jButtonExcluir.setEnabled(false);
            jButtonEditar.setEnabled(false);

        } else {
            Vector<Medico> listaNomes = medicoDAO.consultar(jTextFieldPesquisa.getText());
            Vector conjuntoLinhas = new Vector();

            for (Medico md : listaNomes) {
                Vector linha = new Vector();

                linha.add(md.getNome());
                linha.add(md.getCrm());
                linha.add(md.getEspecialidade());

                conjuntoLinhas.add(linha);

            }
            Vector conjuntoColunas = new Vector();
            conjuntoColunas.add("NOME");
            conjuntoColunas.add("CRM");
            conjuntoColunas.add("ESPECIALIDADE");

            DefaultTableModel tabela = new DefaultTableModel(conjuntoLinhas, conjuntoColunas);

            jTableListaMedico.setModel(tabela);

            jTableListaMedico.getColumnModel().getColumn(0).setResizable(false);
            jTableListaMedico.getColumnModel().getColumn(1).setResizable(false);
            jTableListaMedico.getColumnModel().getColumn(2).setResizable(false);
            jTableListaMedico.getTableHeader().setReorderingAllowed(false);
            jTableListaMedico.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        }

    }//GEN-LAST:event_jButtonPesquisaActionPerformed

