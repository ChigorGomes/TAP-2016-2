    public void listaNome() {
        Vector<Usuario> listaNomes = usuarioDAO.listaTodos();
        Vector conjuntoLinhas = new Vector();
        for (Usuario us : listaNomes) {
            Vector linha = new Vector();
            linha.add(us.getNome());
            linha.add(us.getTipoUsuario());
            conjuntoLinhas.add(linha);
        }
        Vector conjuntoColunas = new Vector();
        conjuntoColunas.add("USUARIO");
        conjuntoColunas.add("TIPO USUARIO");
        DefaultTableModel tabela = new DefaultTableModel(conjuntoLinhas, conjuntoColunas);
        jTableListaUsuario.setModel(tabela);
        jTableListaUsuario.getColumnModel().getColumn(0).setResizable(false);
        jTableListaUsuario.getColumnModel().getColumn(1).setResizable(false);
        jTableListaUsuario.getTableHeader().setReorderingAllowed(false);
        jTableListaUsuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

