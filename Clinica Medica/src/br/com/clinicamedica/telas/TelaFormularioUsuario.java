    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (flag == 1) {
            if (jComboBoxUsuario.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(null, "Informe o tipo de usuário!");
            } else if (jTextFieldNome.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Informe o NOME!");
            } else if (jTextFieldUsuario.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Informe o Usuário!");
            } else if ((new String(jPasswordFieldSenha.getPassword()).equals("")) || (new String(jPasswordFieldSenha2.getPassword()).equals(""))) {
                JOptionPane.showMessageDialog(null, "Informe a senha!");
            } else if (new String(jPasswordFieldSenha.getPassword()).equals(new String(jPasswordFieldSenha2.getPassword()))) {
                usuario.setNome(jTextFieldNome.getText());
                usuario.setUsuario(jTextFieldUsuario.getText());
                usuario.setSenha(new String(jPasswordFieldSenha.getPassword()));
                usuario.setTipoUsuario((String) jComboBoxUsuario.getSelectedItem());
                usuarioDAO.controleAcesso(usuario);
                TelaFormularioUsuario tela = new TelaFormularioUsuario();
                tela.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Senhas não são iguais!");
                jPasswordFieldSenha.setText("");
                jPasswordFieldSenha2.setText("");
                jPasswordFieldSenha.requestFocus();
            }
        } else if ((new String(jPasswordFieldSenha.getPassword()).equals(" ") || new String(jPasswordFieldSenha2.getPassword()).equals(""))) {
            JOptionPane.showMessageDialog(null, "Informe a senha!");
        } else if (new String(jPasswordFieldSenha.getPassword()).equals(new String(jPasswordFieldSenha2.getPassword()))) {
            usuario.setIdUsuario(Integer.parseInt(jTextFieldIdUsuario.getText()));
            usuario.setTipoUsuario((String) jComboBoxUsuario.getSelectedItem());
            usuario.setNome(jTextFieldNome.getText());
            usuario.setUsuario(jTextFieldUsuario.getText());
            usuario.setSenha(new String(jPasswordFieldSenha.getPassword()));
            usuarioDAO.alterar(usuario);
            jTextFieldIdUsuario.setText("");
            jTextFieldNome.setText("");
            jTextFieldUsuario.setText("");
            jComboBoxUsuario.setSelectedIndex(-1);
            jPasswordFieldSenha.setText("");
            jPasswordFieldSenha2.setText("");
            jTextFieldNome.setEnabled(false);
            jTextFieldUsuario.setEnabled(false);
            jComboBoxUsuario.setEnabled(false);
            jPasswordFieldSenha.setEnabled(false);
            jButtonCancelar.setEnabled(true);
            jButtonSalvar.setEnabled(false);
            TelaFormularioUsuario tela = new TelaFormularioUsuario();
            tela.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Senhas não são iguais!");
            jPasswordFieldSenha.setText("");
            jPasswordFieldSenha2.setText("");
            jPasswordFieldSenha.requestFocus();
        }

    }//GEN-LAST:event_jButtonSalvarActionPerformed

