    public TelaConfirmaConsulta() {

        initComponents();
        Calendar data = Calendar.getInstance();
        java.util.Date d = data.getTime();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
        dateFormat.format(d);

        dtHoje = dateFormat.format(d);
        status = "Aberto";

        listaTodos();

    }

    public void listaTodos() {

        Vector<Consulta> listaNomes = consultaDAO.consultar(dtHoje, status);
        Vector conjuntoLinhas = new Vector();

        for (Consulta con : listaNomes) {
            Vector linha = new Vector();
            linha.add(con.getIdConsulta());
            linha.add(con.getNomeMed());
            linha.add(con.getNomePac());
            linha.add(con.getDataConsulta());
            linha.add(con.getHorario());
            linha.add(con.getStatus());

            conjuntoLinhas.add(linha);

        }
        Vector conjuntoColunas = new Vector();
        conjuntoColunas.add("ID");
        conjuntoColunas.add("NOME MEDICO");
        conjuntoColunas.add("NOME PACIENTE");
        conjuntoColunas.add("DATA CONSULTA");
        conjuntoColunas.add("HORARIO");
        conjuntoColunas.add("STATUS");

        DefaultTableModel tabela = new DefaultTableModel(conjuntoLinhas, conjuntoColunas);

        jTableListaConsulta.setModel(tabela);

        jTableListaConsulta.getColumnModel().getColumn(0).setResizable(false);
        jTableListaConsulta.getColumnModel().getColumn(1).setResizable(false);
        jTableListaConsulta.getColumnModel().getColumn(2).setResizable(false);
        jTableListaConsulta.getColumnModel().getColumn(3).setResizable(false);
        jTableListaConsulta.getColumnModel().getColumn(4).setResizable(false);

        jTableListaConsulta.getTableHeader().setReorderingAllowed(false);
        jTableListaConsulta.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

